class BroadcastTo(TensorOp):
    def __init__(self, shape):
        self.shape = shape

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return array_api.broadcast_to(a, self.shape)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        input_shape = node.inputs[0].shape
        res = summation(out_grad, axes=tuple(range(len(out_grad.shape) - len(input_shape))))
        for i in range(len(input_shape)):
            if input_shape[i] == 1:
                res = summation(res, axes=(i,))
        return reshape(res, shape=input_shape)
        ### END YOUR SOLUTION


def broadcast_to(a, shape):
    return BroadcastTo(shape)(a)


class Summation(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        if self.axes is None:
            return array_api.sum(a)
        else:
            valid_axes = tuple(axis for axis in self.axes if axis < len(a.shape))
            if not valid_axes:
                return a
            result = array_api.sum(a, axis=valid_axes)
            return result
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        a = node.inputs[0]
        shape = list(a.shape)
        axes = self.axes
        if axes is None:
            axes = list(range(len(a.shape)))
        for i in range(len(axes)):
           shape[axes[i]] = 1
        return broadcast_to(reshape(out_grad, shape), shape=a.shape)
        ### END YOUR SOLUTION


def summation(a, axes=None):
    return Summation(axes)(a)


class MatMul(TensorOp):
    def compute(self, a, b):
        ### BEGIN YOUR SOLUTION
        # print(f"Shape of a: {a.shape}, ndim: {a.ndim}, type: {type(a)}")
        # print(f"Shape of b: {b.shape}, ndim: {b.ndim}, type: {type(b)}")
        assert isinstance(a, NDArray), "a is not an instance of NDArray"
        assert isinstance(b, NDArray), "b is not an instance of NDArray"
        return a@b
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        a,b = node.inputs
        adj1 = out_grad @ transpose(b)
        adj2 = transpose(a) @ out_grad
        if len(a.shape) == len(b.shape):
            return adj1, adj2
        else:
            adj1 = summation(adj1, axes=tuple(range(len(adj1.shape) - len(a.shape))))
            adj2 = summation(adj2, axes=tuple(range(len(adj2.shape) - len(b.shape))))
            return adj1, adj2
        ### END YOUR SOLUTION


def matmul(a, b):
    return MatMul()(a, b)


